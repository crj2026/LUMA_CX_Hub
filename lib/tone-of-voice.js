export const BRAND_VOICE_SUMMARY =
  "IM8 is a premium health brand built around science, community and long-term wellbeing. The voice should reflect that — it's warm and human, but it's also confident and credible. We're not trying to be everyone's best friend. We're trying to be the brand that actually looks after people. When in doubt: be the kind of person you'd want to hear from when something's gone wrong.";

// Builder — emits the full tone guide as a multi-section block ready to
// drop into any AI system prompt. Keep the formatting consistent so a
// Claude model can lift cleanly from it.
export function buildToneOfVoiceSection() {
  const lines = [];

  lines.push("════════════════════════════════════════════════════════");
  lines.push(`IM8 TONE-OF-VOICE GUIDE (canonical, v${TONE_VERSION})`);
  lines.push("════════════════════════════════════════════════════════");
  lines.push(
    "Use this for ALL customer-facing copy you draft. It is the authoritative voice. If anything elsewhere in this prompt conflicts with it, this section wins."
  );
  lines.push("");

  lines.push("VOICE IN ONE SENTENCE");
  lines.push(VOICE_ONE_SENTENCE);
  lines.push("");

  lines.push("CORE PRINCIPLES");
  for (const p of CORE_PRINCIPLES) {
    lines.push(`- ${p.name}: ${p.body}`);
  }
  lines.push("");

  lines.push("OPENERS");
  lines.push("Avoid:");
  for (const x of OPENERS.avoid) lines.push(`  ❌ "${x}"`);
  lines.push("Use:");
  for (const x of OPENERS.use) lines.push(`  ✅ "${x}"`);
  lines.push("");

  lines.push("BODY");
  lines.push("Avoid:");
  for (const x of BODY_RULES.avoid) lines.push(`  ❌ ${x}`);
  lines.push("Use:");
  for (const x of BODY_RULES.use) lines.push(`  ✅ ${x}`);
  lines.push("");

  lines.push("CLOSERS");
  lines.push("Avoid:");
  for (const x of CLOSERS.avoid) lines.push(`  ❌ "${x}"`);
  lines.push("Use:");
  for (const x of CLOSERS.use) lines.push(`  ✅ "${x}"`);
  lines.push("");
  lines.push(`Sign-offs: ${CLOSERS.signOff}`);
  lines.push("");

  lines.push("LENGTH GUIDE");
  for (const r of LENGTH_GUIDE) {
    lines.push(`- ${r.ticketType}: ${r.target}`);
  }
  lines.push(`Test: ${LENGTH_TEST}`);
  lines.push("");

  lines.push("NEVER SAY THESE PHRASES");
  for (const r of NEVER_SAY_PHRASES) {
    lines.push(`- "${r.phrase}" — ${r.why}`);
  }
  lines.push("");

  lines.push("APOLOGIES — IMPORTANT");
  lines.push(APOLOGY_RULE.headline);
  lines.push("Avoid:");
  for (const x of APOLOGY_RULE.avoid) lines.push(`  ❌ "${x}"`);
  lines.push("Use:");
  for (const x of APOLOGY_RULE.use) lines.push(`  ✅ "${x}"`);
  lines.push("");
  lines.push(APOLOGY_RULE.operational.note);
  for (const x of APOLOGY_RULE.operational.use) lines.push(`  ✅ "${x}"`);
  lines.push("");
  lines.push(`Exception: ${APOLOGY_RULE.exception}`);
  lines.push("");

  lines.push("DRAMATISING SYMPTOMS");
  lines.push(DRAMATISING_RULE.headline);
  lines.push("Avoid:");
  for (const x of DRAMATISING_RULE.avoid) lines.push(`  ❌ "${x}"`);
  lines.push("Use:");
  for (const x of DRAMATISING_RULE.use) lines.push(`  ✅ "${x}"`);
  lines.push("");
  lines.push(`Formula: ${DRAMATISING_RULE.formula}`);
  lines.push("");

  lines.push("ADVERSE-REACTION REPLIES — INTERNAL vs CUSTOMER-FACING");
  lines.push(ADVERSE_REACTION_REPLY_RULES.headline);
  lines.push("");
  lines.push("INTERNAL ONLY (for the agent — never write any of these to the customer):");
  for (const x of ADVERSE_REACTION_REPLY_RULES.internalOnly) lines.push(`  • ${x}`);
  lines.push("");
  lines.push("CUSTOMER-FACING SHAPE (this is the reply shape — nothing more, nothing less):");
  for (const x of ADVERSE_REACTION_REPLY_RULES.customerFacingShape) lines.push(`  ${x}`);
  lines.push("");
  lines.push("Avoid in the customer-facing reply:");
  for (const x of ADVERSE_REACTION_REPLY_RULES.avoid) lines.push(`  ❌ "${x}"`);
  lines.push("Use:");
  for (const x of ADVERSE_REACTION_REPLY_RULES.use) lines.push(`  ✅ "${x}"`);
  lines.push("");
  lines.push(`Pre-existing condition note: ${ADVERSE_REACTION_REPLY_RULES.pretextSensitivityNote}`);
  lines.push("");

  lines.push("REAL EXAMPLES OF THE TONE");
  for (const ex of REAL_EXAMPLES) {
    lines.push(`— ${ex.title} —`);
    lines.push(ex.body);
    lines.push(`Why it works: ${ex.whyItWorks}`);
    lines.push("");
  }

  lines.push("FIVE QUESTIONS BEFORE YOU SEND (if any answer is no, rewrite)");
  FIVE_QUESTIONS.forEach((q, i) => lines.push(`${i + 1}. ${q}`));
  lines.push("");

  lines.push("BRAND VOICE SUMMARY");
  lines.push(BRAND_VOICE_SUMMARY);
  lines.push("");

  lines.push("════════════════════════════════════════════════════════");
  lines.push("END TONE GUIDE");
  lines.push("════════════════════════════════════════════════════════");

  return lines.join("\n");
}
